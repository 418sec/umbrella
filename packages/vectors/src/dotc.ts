/**
 * Returns pairwise product sum of given components.
 *
 * @param a -
 * @param b -
 * @param c -
 * @param d -
 */
export const dotC4 = (a: number, b: number, c: number, d: number) =>
    a * b + c * d;

/**
 * Returns pairwise product sum of given components.
 *
 * @param a -
 * @param b -
 * @param c -
 * @param d -
 * @param e -
 * @param f -
 */
export const dotC6 = (
    a: number,
    b: number,
    c: number,
    d: number,
    e: number,
    f: number
) => a * b + c * d + e * f;

